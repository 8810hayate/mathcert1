// =============================================
// アプリケーション ロジック
// =============================================

class QuizApp {
    constructor() {
        this.allQuestions = QUESTIONS;
        this.filteredQuestions = [...QUESTIONS];
        this.currentIndex = 0;
        this.isFlipped = false;
        this.mode = 'sequential';
        this.currentCategory = 'all';
        this.correctCount = 0;
        this.streak = 0;
        this.maxStreak = 0;
        this.answeredQuestions = new Set();
        this.reviewQuestions = new Set();
        this.masteredQuestions = new Set();
        this.questionOrder = [];

        this.loadProgress();
        this.initDOM();
        this.initCategories();
        this.initEventListeners();
        this.filterQuestions();
        this.updateCategoryProgress();
    }

    saveProgress() {
        try {
            const data = {
                mastered: [...this.masteredQuestions],
                review: [...this.reviewQuestions],
                answered: [...this.answeredQuestions],
                correctCount: this.correctCount,
                maxStreak: this.maxStreak
            };
            localStorage.setItem('math1_progress', JSON.stringify(data));
        } catch (e) { /* ignore */ }
    }

    loadProgress() {
        try {
            const raw = localStorage.getItem('math1_progress');
            if (!raw) return;
            const data = JSON.parse(raw);
            this.masteredQuestions = new Set(data.mastered || []);
            this.reviewQuestions = new Set(data.review || []);
            this.answeredQuestions = new Set(data.answered || []);
            this.correctCount = data.correctCount || 0;
            this.maxStreak = data.maxStreak || 0;
        } catch (e) { /* ignore */ }
    }

    resetAllProgress() {
        this.masteredQuestions.clear();
        this.reviewQuestions.clear();
        this.answeredQuestions.clear();
        this.correctCount = 0;
        this.streak = 0;
        this.maxStreak = 0;
        this.saveProgress();
        this.filterQuestions();
        this.updateCategoryProgress();
    }

    initDOM() {
        this.cardInner = document.getElementById('card-inner');
        this.cardQuestion = document.getElementById('card-question');
        this.cardAnswer = document.getElementById('card-answer');
        this.cardSupplement = document.getElementById('card-supplement');
        this.cardCatTag = document.getElementById('card-cat-tag');
        this.cardNumber = document.getElementById('card-number');
        this.flashcard = document.getElementById('flashcard');
        this.cardHint = document.getElementById('card-hint');
        this.cardAnswerSection = document.getElementById('card-answer-section');

        this.assessment = document.getElementById('assessment');

        this.navPrev = document.getElementById('nav-prev');
        this.navNext = document.getElementById('nav-next');
        this.progressBar = document.getElementById('progress-bar');
        this.progressText = document.getElementById('progress-text');
        this.progressValue = document.getElementById('progress-value');
        this.correctValue = document.getElementById('correct-value');
        this.streakValue = document.getElementById('streak-value');

        this.categoryProgressGrid = document.getElementById('category-progress-grid');

        this.completionModal = document.getElementById('completion-modal');
        this.modalText = document.getElementById('modal-text');
        this.modalStats = document.getElementById('modal-stats');
        this.modalRestart = document.getElementById('modal-restart');
    }

    initCategories() {
        const categories = [...new Set(this.allQuestions.map(q => q.category))];
        const scroll = document.querySelector('.categories-scroll');

        categories.forEach(cat => {
            const icon = this.allQuestions.find(q => q.category === cat)?.icon || '📌';
            const btn = document.createElement('button');
            btn.className = 'cat-btn';
            btn.dataset.category = cat;
            btn.id = `cat-${cat}`;
            btn.innerHTML = `<span class="cat-icon">${icon}</span>${cat}`;
            scroll.appendChild(btn);
        });
    }

    initEventListeners() {
        this.flashcard.addEventListener('click', () => this.toggleReveal());

        document.addEventListener('keydown', (e) => {
            switch (e.key) {
                case ' ':
                case 'Enter':
                    e.preventDefault();
                    this.toggleReveal();
                    break;
                case 'ArrowLeft':
                    e.preventDefault();
                    this.prevQuestion();
                    break;
                case 'ArrowRight':
                    e.preventDefault();
                    this.nextQuestion();
                    break;
                case '1':
                    if (this.isFlipped) this.assess('bad');
                    break;
                case '2':
                    if (this.isFlipped) this.assess('ok');
                    break;
                case '3':
                    if (this.isFlipped) this.assess('good');
                    break;
            }
        });

        this.navPrev.addEventListener('click', () => this.prevQuestion());
        this.navNext.addEventListener('click', () => this.nextQuestion());

        document.querySelector('.categories-scroll').addEventListener('click', (e) => {
            const btn = e.target.closest('.cat-btn');
            if (!btn) return;
            document.querySelectorAll('.cat-btn').forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            this.currentCategory = btn.dataset.category;
            this.filterQuestions();
        });

        document.querySelectorAll('.mode-btn').forEach(btn => {
            btn.addEventListener('click', () => {
                document.querySelectorAll('.mode-btn').forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
                this.mode = btn.dataset.mode;
                this.filterQuestions();
            });
        });

        document.getElementById('assess-bad').addEventListener('click', () => this.assess('bad'));
        document.getElementById('assess-ok').addEventListener('click', () => this.assess('ok'));
        document.getElementById('assess-good').addEventListener('click', () => this.assess('good'));

        this.modalRestart.addEventListener('click', () => {
            this.completionModal.classList.add('hidden');
            this.streak = 0;
            this.filterQuestions();
        });

        const resetBtn = document.getElementById('reset-progress');
        if (resetBtn) {
            resetBtn.addEventListener('click', () => {
                if (confirm('すべての学習進捗をリセットしますか？\n（完璧・復習の記録がすべて消えます）')) {
                    this.resetAllProgress();
                }
            });
        }

        let startX = 0;
        this.flashcard.addEventListener('touchstart', (e) => {
            startX = e.changedTouches[0].screenX;
        }, { passive: true });

        this.flashcard.addEventListener('touchend', (e) => {
            const endX = e.changedTouches[0].screenX;
            const diff = endX - startX;
            if (Math.abs(diff) > 80) {
                if (diff > 0) this.prevQuestion();
                else this.nextQuestion();
            }
        }, { passive: true });
    }

    filterQuestions() {
        const catFilter = q => this.currentCategory === 'all' || q.category === this.currentCategory;

        if (this.mode === 'review') {
            this.filteredQuestions = this.allQuestions.filter(q =>
                this.reviewQuestions.has(this.allQuestions.indexOf(q)) && catFilter(q)
            );
        } else {
            this.filteredQuestions = this.allQuestions.filter(q =>
                !this.masteredQuestions.has(this.allQuestions.indexOf(q)) && catFilter(q)
            );
        }

        if (this.filteredQuestions.length === 0) {
            if (this.mode === 'review') {
                this.filteredQuestions = this.allQuestions.filter(catFilter);
                if (this.filteredQuestions.length === 0) {
                    this.filteredQuestions = [...this.allQuestions];
                }
                this.showAllMasteredMessage();
            } else {
                this.filteredQuestions = this.allQuestions.filter(catFilter);
                if (this.filteredQuestions.length === 0) {
                    this.filteredQuestions = [...this.allQuestions];
                }
                this.showAllMasteredMessage();
            }
        }

        this.currentIndex = 0;
        this.resetQuestionOrder();
        this.showQuestion();
        this.updateMasteredCounter();
    }

    showAllMasteredMessage() {
        const catMastered = this.filteredQuestions.every(q =>
            this.masteredQuestions.has(this.allQuestions.indexOf(q))
        );
        if (catMastered && this.masteredQuestions.size > 0) {
            setTimeout(() => {
                if (this.filteredQuestions.length > 0) {
                    this.cardQuestion.textContent = '🎉 このカテゴリの全問題を完璧にマスターしました！';
                    this.cardHint.innerHTML = '<span>もう一度解きたい場合は進捗をリセットしてください</span>';
                }
            }, 100);
        }
    }

    updateMasteredCounter() {
        const counter = document.getElementById('mastered-value');
        if (counter) {
            counter.textContent = this.masteredQuestions.size;
        }
    }

    resetQuestionOrder() {
        this.questionOrder = Array.from({ length: this.filteredQuestions.length }, (_, i) => i);
        if (this.mode === 'random') {
            this.shuffleArray(this.questionOrder);
        }
    }

    shuffleArray(arr) {
        for (let i = arr.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [arr[i], arr[j]] = [arr[j], arr[i]];
        }
    }

    toggleReveal() {
        this.isFlipped = !this.isFlipped;
        if (this.isFlipped) {
            this.cardAnswerSection.classList.remove('hidden');
            this.cardAnswerSection.classList.add('visible');
            this.cardInner.classList.add('revealed');
            this.cardHint.classList.add('hidden');
        } else {
            this.cardAnswerSection.classList.remove('visible');
            this.cardAnswerSection.classList.add('hidden');
            this.cardInner.classList.remove('revealed');
            this.cardHint.classList.remove('hidden');
        }
        this.assessment.classList.toggle('hidden', !this.isFlipped);
    }

    showQuestion() {
        if (this.filteredQuestions.length === 0) return;

        const idx = this.questionOrder[this.currentIndex];
        const q = this.filteredQuestions[idx];

        this.isFlipped = false;
        this.cardAnswerSection.classList.remove('visible');
        this.cardAnswerSection.classList.add('hidden');
        this.cardInner.classList.remove('revealed');
        this.cardHint.classList.remove('hidden');
        this.assessment.classList.add('hidden');

        this.flashcard.classList.remove('entering');
        void this.flashcard.offsetWidth;
        this.flashcard.classList.add('entering');

        this.cardQuestion.textContent = q.question;
        this.cardAnswer.textContent = q.answer;
        this.cardSupplement.textContent = q.supplement || '';
        this.cardCatTag.textContent = `${q.icon} ${q.category}`;

        const globalIdx = this.allQuestions.indexOf(q) + 1;
        this.cardNumber.textContent = `Q.${globalIdx}`;

        this.updateProgress();
    }

    updateProgress() {
        const total = this.filteredQuestions.length;
        const current = this.currentIndex + 1;

        this.progressText.textContent = `${current} / ${total}`;
        this.progressBar.style.width = `${(current / total) * 100}%`;
        this.progressValue.textContent = `${this.answeredQuestions.size}/${this.allQuestions.length}`;
        this.correctValue.textContent = this.correctCount;
        this.streakValue.textContent = this.streak;

        this.navPrev.disabled = this.currentIndex === 0;
    }

    nextQuestion() {
        if (this.currentIndex < this.filteredQuestions.length - 1) {
            this.currentIndex++;
            this.showQuestion();
        }
    }

    prevQuestion() {
        if (this.currentIndex > 0) {
            this.currentIndex--;
            this.showQuestion();
        }
    }

    assess(level) {
        const idx = this.questionOrder[this.currentIndex];
        const globalIdx = this.allQuestions.indexOf(this.filteredQuestions[idx]);

        this.answeredQuestions.add(globalIdx);

        if (level === 'good') {
            this.correctCount++;
            this.streak++;
            if (this.streak > this.maxStreak) this.maxStreak = this.streak;
            this.masteredQuestions.add(globalIdx);
            this.reviewQuestions.delete(globalIdx);
        } else if (level === 'bad') {
            this.streak = 0;
            this.masteredQuestions.delete(globalIdx);
            this.reviewQuestions.add(globalIdx);
        } else {
            this.masteredQuestions.delete(globalIdx);
            this.reviewQuestions.add(globalIdx);
        }

        this.saveProgress();
        this.updateCategoryProgress();
        this.updateMasteredCounter();

        if (this.currentIndex >= this.filteredQuestions.length - 1) {
            this.showCompletion();
        } else {
            this.nextQuestion();
        }
    }

    showCompletion() {
        this.completionModal.classList.remove('hidden');
        const total = this.filteredQuestions.length;
        this.modalText.textContent = `${total}問を学習しました！`;
        this.modalStats.innerHTML = `
            <div class="modal-stat">
                <div class="modal-stat-value">${this.correctCount}</div>
                <div class="modal-stat-label">完璧</div>
            </div>
            <div class="modal-stat">
                <div class="modal-stat-value">${this.maxStreak}</div>
                <div class="modal-stat-label">最大連続</div>
            </div>
            <div class="modal-stat">
                <div class="modal-stat-value">${this.reviewQuestions.size}</div>
                <div class="modal-stat-label">要復習</div>
            </div>
        `;
    }

    updateCategoryProgress() {
        const categories = [...new Set(this.allQuestions.map(q => q.category))];
        this.categoryProgressGrid.innerHTML = '';

        categories.forEach(cat => {
            const catQuestions = this.allQuestions.filter(q => q.category === cat);
            const total = catQuestions.length;
            const answered = catQuestions.filter(q => {
                const idx = this.allQuestions.indexOf(q);
                return this.answeredQuestions.has(idx);
            }).length;
            const mastered = catQuestions.filter(q => {
                const idx = this.allQuestions.indexOf(q);
                return this.masteredQuestions.has(idx);
            }).length;

            const icon = catQuestions[0]?.icon || '📌';
            const pct = total > 0 ? (mastered / total) * 100 : 0;
            const isMastered = mastered === total && total > 0;

            const card = document.createElement('div');
            card.className = 'cat-progress-card';
            card.innerHTML = `
                <div class="cat-progress-header">
                    <span class="cat-progress-name">${icon} ${cat}</span>
                    <span class="cat-progress-count">${mastered}/${total} 完璧</span>
                </div>
                <div class="cat-progress-bar-bg">
                    <div class="cat-progress-bar-fill ${isMastered ? 'mastered' : ''}" style="width: ${pct}%"></div>
                </div>
            `;
            this.categoryProgressGrid.appendChild(card);
        });
    }
}

document.addEventListener('DOMContentLoaded', () => {
    new QuizApp();
});
